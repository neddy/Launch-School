# There are a number of variables listed below. What are the different types and how do you know which is which?

excited_dog = "excited dog" # Local variable. This can be seen because the variable is in lowercase, and does not begin with a `@`
@excited_dog = "excited dog" # Instance variable. Instance variables begin with one `@`
@@excited_dog = "excited dog" # Class variable. Class variables start with two `@`
