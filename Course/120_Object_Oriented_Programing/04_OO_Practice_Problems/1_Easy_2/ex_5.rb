# ex_5.rb
# There are a number of variables listed below. What are the different types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

#Answer
# The first is a local variable, we know this because it is not prefixed with anything.
#
# The next is an instance variable, this is known because instance variables start with a `@`
#
# The last one is a class variable, this is know because class variables start with `@@`
